INSERT INTO segue (user_seguidor, user_seguido)
SELECT p1.id_perfil, p2.id_perfil
FROM perfil p1
JOIN perfil p2
  ON p2.id_perfil BETWEEN p1.id_perfil + 1 AND p1.id_perfil + 5
WHERE p1.id_perfil <= 95;


INSERT INTO conversa ()
SELECT NULL
FROM segue
ORDER BY user_seguidor, user_seguido;


CREATE TEMPORARY TABLE temp_mapeamento AS
SELECT
    s.user_seguidor,
    s.user_seguido,
    c.id_conversa
FROM (
    SELECT
        user_seguidor,
        user_seguido,
        ROW_NUMBER() OVER (ORDER BY user_seguidor, user_seguido) AS rn
    FROM segue
) s
JOIN (
    SELECT
        id_conversa,
        ROW_NUMBER() OVER (ORDER BY id_conversa) AS rn
    FROM conversa
) c ON s.rn = c.rn;


INSERT INTO participa (id_perfil, id_conversa)
